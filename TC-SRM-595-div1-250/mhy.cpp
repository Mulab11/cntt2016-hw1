class LittleElephantAndIntervalsDiv1 {
    public:
        int a[1005];
        long long getNumber(int n, vector<int> L, vector<int> R) {
            memset(a,-1,sizeof(a));
            int m=L.size();
            long long ans=1;
            for (int i=0;i<m;i++) for (int j=L[i];j<=R[i];j++) a[j-1]=i;
            for (int i=0;i<m;i++){
                int tag=0;
                for (int j=0;j<n;j++) if (a[j]==i) tag=1;
                if (tag) ans<<=1;
            }
            return ans;
        }
};

