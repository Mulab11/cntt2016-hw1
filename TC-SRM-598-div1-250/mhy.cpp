class BinPacking {
    public:
    	int tag[233];
    	int minBins(vector<int> item) {
    		memset(tag,0,sizeof(tag));
    		int n=item.size();
    		sort(item.rbegin(),item.rend());
    		int ans=0;
    		for (int i=0;i<n;i++) if (!tag[i]&&item[i]>100){
    			tag[i]=1;
    			for (int j=i+1;j<n;j++) if (!tag[j]&&item[i]+item[j]<=300){
    				tag[j]=1;
    				break;
    			}
    			ans++;
    		}
    		int sum=0;
    		for (int i=0;i<n;i++) if (!tag[i]) sum++;
        	return ans+(sum+2)/3;
    	}
};

