class MayTheBestPetWin {
    public:
        int s1,s2;
        bitset<1000005> dp;
        inline int calc(int s){return max(s-s1,s2-s);}
        int calc(vector<int> A, vector<int> B) {
            int n=A.size();
            for (int i=0;i<n;i++) s1+=A[i];
            for (int i=0;i<n;i++) s2+=B[i];
            dp.set(0);
            for (int i=0;i<n;i++) dp|=dp<<(A[i]+B[i]);
            int ans=0x3f3f3f3f;
            for (int i=0;i<1000000;i++) if (dp.test(i)) ans=min(ans,calc(i));
            return ans;
        }
};

