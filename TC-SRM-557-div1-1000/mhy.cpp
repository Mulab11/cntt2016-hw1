class XorAndSum {
    public:
        long long bit[105];
        long long maxSum(vector<long long> number) {
            memset(bit,0,sizeof(bit));
            int n=number.size();
            long long maxbit=0;
            for (int j=60;j--;) for (int i=0;i<n;i++) if (!bit[i]&&number[i]>>j&1){
                if (!maxbit) maxbit=1ll<<j;
                bit[i]=1ll<<j;
                for (int k=0;k<n;k++) if (i!=k) if (number[k]&bit[i]) number[k]^=number[i];
                break;
            }
            long long sum=0;
            for (int i=0;i<n;i++) if (bit[i]) sum^=number[i];
            long long ans=sum;
            for (int i=0;i<n;i++) if (bit[i]<maxbit) ans+=sum^number[i];
            return ans;
        }
};

